package collaborative_exams;

public class Answer {

	private int Answer;
	private String text;
	
	
	

	public int getAnswer() {
		return Answer;
	}

	public void setAnswer(int answer) {
		Answer = answer;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}
